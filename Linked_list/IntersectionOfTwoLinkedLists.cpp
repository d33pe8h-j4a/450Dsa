Node* findIntersection(Node* head1, Node* head2)
{
    // Your Code Here
    vector<int> v1, v2;
    Node* curr = head1;
    while (curr)
    {
        v1.push_back(curr->data);
        curr = curr->next;
    }
    sort(v1.begin(), v1.end());
    curr = head2;
    while (curr)
    {
        if (find(v1.begin(), v1.end(), curr->data) != v1.end()) 
            v2.push_back(curr->data);
    }
    Node* head = NULL;
    Node* tail = NULL;
    for (auto &&i : v2)
    {
        Node* new_node = new Node(i);
        if (not head)
        {
            head = new_node;
            tail = new_node;
        }
        else
        {
            tail->next = new_node;
            tail = new_node;
        }
    }
    return head;
}